/**
 * Generate a minimal valid PNG in memory. No binary-processing dependency.
 *
 * The bytes below encode a 1×1 PNG: signature + IHDR (width 1, height 1,
 * 8-bit greyscale) + IDAT (zlib-wrapped single black pixel) + IEND.
 * Telegram accepts this as a photo/document upload.
 */

const PNG_1X1_BLACK: readonly number[] = [
  0x89,
  0x50,
  0x4e,
  0x47,
  0x0d,
  0x0a,
  0x1a,
  0x0a, // PNG signature
  0x00,
  0x00,
  0x00,
  0x0d,
  0x49,
  0x48,
  0x44,
  0x52, // IHDR length + type
  0x00,
  0x00,
  0x00,
  0x01,
  0x00,
  0x00,
  0x00,
  0x01, // width=1, height=1
  0x08,
  0x00,
  0x00,
  0x00,
  0x00,
  0x3b,
  0x7e,
  0x9b, // 8-bit grey, crc
  0x55,
  0x00,
  0x00,
  0x00,
  0x0a,
  0x49,
  0x44,
  0x41, // IDAT length + type
  0x54,
  0x78,
  0x9c,
  0x62,
  0x00,
  0x00,
  0x00,
  0x00, // deflate stream
  0x05,
  0x00,
  0x01,
  0x0d,
  0x0a,
  0x2d,
  0xb4,
  0x00, // IDAT crc
  0x00,
  0x00,
  0x00,
  0x49,
  0x45,
  0x4e,
  0x44,
  0xae, // IEND
  0x42,
  0x60,
  0x82,
];

export function generate1x1Png(): Buffer {
  return Buffer.from(PNG_1X1_BLACK);
}
